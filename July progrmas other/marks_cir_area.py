# area of a circle.
def AreaCir(a):
    '''Calculating the area'''
    b = 3.14 * a * a
    '''Printing area'''
    print("The area of your circle is " + str(b))

#circumfrence of a circle.
def CircumfrenceCir(a):
    '''Calculating the Circumfrence'''
    b = 3.14 * (a*2)
    '''Printing circumfrence'''
    print("The circumfrence of your circle is " + str(b))
